    def test_parse_returns_results(self):
        """Parser should return at least one ExtractedClause."""
        from app.parsers.docx_parser import parse_docx

        docx_path = _ensure_fixture()
        file_bytes = docx_path.read_bytes()
        results = parse_docx(file_bytes)
        assert len(results) > 0, "Expected at least one extracted clause"

    def test_anchor_text_extracted(self):
        """Anchor text should contain the highlighted phrase."""
        from app.parsers.docx_parser import parse_docx

        docx_path = _ensure_fixture()
        file_bytes = docx_path.read_bytes()
        results = parse_docx(file_bytes)

        anchor_texts = [r.anchor_text for r in results]
        # The comment is on "liable for"
        assert any("liable for" in t for t in anchor_texts), (
            f"Expected 'liable for' in anchor texts, got: {anchor_texts}"
        )

    def test_clause_expansion_to_numbered_subclause(self):
        """Clause should expand to the 12.3(a) numbered subclause, not the whole section."""
        from app.parsers.docx_parser import parse_docx

        docx_path = _ensure_fixture()
        file_bytes = docx_path.read_bytes()
        results = parse_docx(file_bytes)

        # Find the result with "liable for" anchor
        matching = [r for r in results if "liable for" in r.anchor_text]
        assert matching, "No result found for 'liable for' anchor"

        result = matching[0]
        # Clause number should be the specific subclause 12.3(a)
        assert result.clause_number is not None, "Expected a clause_number"
        assert "12.3" in result.clause_number or "12" in result.clause_number, (
            f"Expected numbered subclause, got: {result.clause_number}"
        )
        # Expansion method should be numbered_subclause, not paragraph
        assert result.expansion_method in ("numbered_subclause", "boundary_merge"), (
            f"Expected numbered expansion, got: {result.expansion_method}"
        )

    def test_comment_text_extracted(self):
        """Comment text should be extracted from comments.xml."""
        from app.parsers.docx_parser import parse_docx

        docx_path = _ensure_fixture()
