class TestClausePatterns:
    def test_extract_numbered_patterns(self):
        """Clause number extraction should handle all common patterns."""
        from app.parsers.clause_patterns import extract_clause_number

        test_cases = [
            ("12.3(a) Some text", "12.3(a)"),
            ("12.3.1 Some text", "12.3.1"),
            ("12.3 Some text", "12.3"),
            ("12. Some text", "12."),
            ("(a) Some text", "(a)"),
            ("Section 5. Some text", "Section 5."),
            ("No number here", None),
        ]
        for text, expected in test_cases:
            result = extract_clause_number(text)
            if expected is None:
                assert result is None, f"Expected None for '{text}', got: {result}"
            else:
                assert result is not None, f"Expected '{expected}' for '{text}', got None"
                assert expected.rstrip(".") in result or result.rstrip(".") in expected, (
                    f"Expected '{expected}' for '{text}', got: {result}"
                )

    def test_specificity_scoring(self):
        """More specific clause numbers should have higher scores."""
        from app.parsers.clause_patterns import specificity_score

        assert specificity_score("12.3(a)") > specificity_score("12.3")
        assert specificity_score("12.3") > specificity_score("12.")
        assert specificity_score("12.") > specificity_score(None)
        assert specificity_score("12.3.1(a)") >= specificity_score("12.3(a)")
